let colors = ["red","yello","Orange","Pink","White"];
console.log(colors);

// Array(5) [ "red", "yello", "Orange", "Pink", "White" ]
console.log(colors.slice());
// Array(5) [ "red", "yello", "Orange", "Pink", "White" ]

console.log(colors.slice(2));
// Array(3) [ "Orange", "Pink", "White" ]

console.log(colors.slice(2,3));
// Array [ "Orange" ]

console.log(colors.slice(-2));
// Array [ "Pink", "White" ]








